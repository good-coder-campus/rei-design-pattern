package command.receiver

class GarageDoor {
    fun up() {
        println("Garage door is open")
    }

    fun down() {
        println("Garage door is closed")
    }

    fun stop() {
        println("Garage door is stopped")
    }

    fun lightOn() {
        println("Garage light is on")
    }

    fun lightOff() {
        println("Garage light is off")
    }
}